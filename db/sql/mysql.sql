  `responsetimelast7` varchar(50) DEFAULT NULL,
  `dateUpdated` datetime DEFAULT NULL,
  `dateLaststats` datetime DEFAULT NULL,
  `dateCreated` datetime DEFAULT NULL,
  `longversion` varchar(20) DEFAULT NULL,
