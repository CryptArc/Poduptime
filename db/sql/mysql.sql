  `hidden` varchar(3) DEFAULT 'yes',
  `ip` varchar(50) DEFAULT NULL,
  `country` varchar(50) DEFAULT NULL,
  `city` varchar(50) DEFAULT NULL,
  `state` varchar(50) DEFAULT NULL,
  `lat` varchar(50) DEFAULT NULL,
  `long` varchar(50) DEFAULT NULL,
  `connection` varchar(50) DEFAULT NULL,
  `email` varchar(50) DEFAULT NULL,
  `ipv6` varchar(50) DEFAULT NULL,
  `secure` varchar(50) DEFAULT NULL,
  `sslvalid` varchar(50) DEFAULT NULL,
  `Hgitdate` datetime DEFAULT NULL,
  `Hgitref` varchar(50) DEFAULT NULL,
  `Hruntime` varchar(50) DEFAULT NULL,
  `Hencoding` varchar(50) DEFAULT NULL,
  `pingdomurl` varchar(50) DEFAULT NULL,
  `pingdomlast` varchar(50) DEFAULT NULL,
  `monthsmonitored` int(11) DEFAULT NULL,
  `signup` tinyint(4) DEFAULT NULL,
  `total_users` int(11) DEFAULT NULL,
  `active_users_halfyear` int(11) DEFAULT NULL,
  `active_users_monthly` int(11) DEFAULT NULL,
